-- wineapp という名前のデータベースを作成します。
-- IF NOT EXISTS を付けることで、もし既に同じ名前のデータベースが存在してい-- てもエラーにならず、安全に処理をスキップできます。
-- このデータベースで扱う文字コードを utf8mb4 に指定しています。
CREATE DATABASE IF NOT EXISTS wineapp 
CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

-- これから実行する操作を wineapp データベースに対して行います。
USE wineapp;

-- 接続するクライアントとデータベースサーバーとの間での「会話ルール」を決定します。
SET NAMES utf8mb4;

CREATE TABLE IF NOT EXISTS wines (
    -- ワインID: 自動採番される主キー（各ワインを一意に識別する）
    id INT AUTO_INCREMENT PRIMARY KEY,
    
    -- ワイン名: 空欄不可、最大100文字
    name VARCHAR(100) NOT NULL,
    
    -- 生産国: 最大50文字
    country VARCHAR(50),

    region VARCHAR(50), 

    -- 地区
    district VARCHAR(50),

    -- 年代
    vintage INT,
    
    -- 色
    color VARCHAR(20),

    -- 在庫数: 整数型、デフォルト値は0（在庫を管理する）
    quantity INT DEFAULT 0,
    
    -- 原価: 整数型、空欄不可（仕入れ値）
    
    cost_price INT NOT NULL,
    
    -- 販売価格: 整数型、空欄不可（売り値）
    selling_price INT NOT NULL,

    description TEXT,

    grape_variety VARCHAR(100),

    pairing VARCHAR(100),

    label_image_url VARCHAR(255) DEFAULT 'default.jpg'

) CHARACTER SET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

